#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;
	size_t	len;
	int		i;

	len = ft_strlen(s1);
	tmp = (char *)malloc((len * sizeof(s1[0])) + 1);
	i = 0;
	if (!tmp)
		return (NULL);
	while (s1[i])
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = 0;
	return (tmp);
}
